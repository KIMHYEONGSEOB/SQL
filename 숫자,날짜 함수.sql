			 TRUNC(SYSDATE)일, -- 일 기준으로 절삭
			 TRUNC(SYSDATE, 'MONTH')월, -- 월 기준으로 절삭
			 TRUNC(SYSDATE, 'YEAR')년
FROM DUAL; 